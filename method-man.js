function words(value){
    return value.split(" ");
}
function sentence(value) {
    return value.join(" ");
}

function yell(value) {
    return value.toUpperCase;
}
function whisper(value) {
    return value.toLowerCase("*");
}
function capitalize(value) {
    return value.charAt(0).toUpperCase() + value.slice(1).toLowerCase();
}

